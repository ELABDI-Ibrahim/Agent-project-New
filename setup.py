"""
Setup script for the multi-agent data analyzer package.
"""

from setuptools import setup, find_packages

with open("requirements.txt", "r") as f:
    requirements = [line.strip() for line in f if line.strip() and not line.startswith("#")]

setup(
    name="multi-agent-analyzer",
    version="0.1.0",
    description="A sophisticated system that leverages specialized AI agents to process CSV data and generate comprehensive analytical reports",
    author="Multi-Agent Data Analyzer Team",
    packages=find_packages(),
    install_requires=requirements,
    python_requires=">=3.8",
    classifiers=[
        "Development Status :: 3 - Alpha",
        "Intended Audience :: Developers",
        "License :: OSI Approved :: MIT License",
        "Programming Language :: Python :: 3",
        "Programming Language :: Python :: 3.8",
        "Programming Language :: Python :: 3.9",
        "Programming Language :: Python :: 3.10",
        "Programming Language :: Python :: 3.11",
    ],
    entry_points={
        "console_scripts": [
            "multi-agent-analyzer=multi_agent_analyzer.main:main",
        ],
    },
)